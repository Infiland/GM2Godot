from __future__ import annotations

from dataclasses import dataclass
from typing import Iterable, Literal, TypeAlias

GMLAPISupportStatus: TypeAlias = Literal[
    "implemented",
    "partial",
    "planned",
    "unsupported",
    "out_of_scope",
]
GMLAPISupportFlag: TypeAlias = Literal["yes", "partial", "no", "n/a"]


@dataclass(frozen=True)
class GMLAPIEntry:
    name: str
    category: str
    status: GMLAPISupportStatus
    issue_number: int
    owner_module: str
    parser_support: GMLAPISupportFlag
    emitter_support: GMLAPISupportFlag
    runtime_support: GMLAPISupportFlag
    smoke_coverage: GMLAPISupportFlag
    docs_url: str
    notes: str


@dataclass(frozen=True)
class GMLAPICategoryReport:
    category: str
    issue_number: int
    implemented: int
    partial: int
    planned: int
    unsupported: int
    out_of_scope: int

    @property
    def total(self) -> int:
        return (
            self.implemented
            + self.partial
            + self.planned
            + self.unsupported
            + self.out_of_scope
        )


